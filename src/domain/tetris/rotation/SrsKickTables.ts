export class SrsKickTableProvider implements KickTableProvider {
  getKickTable(pieceType: TetrominoType, direction: RotationDirection): KickTable {
    if (direction === "ONE_EIGHTY") return {};
    if (pieceType === "I") return srsIKickTable;
    if (pieceType === "O") return srsOKickTable;
    return srsJlstzKickTable;
  }
}
